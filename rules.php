<h5><b>Advertise for real, and only for yourself</b></h5>

<span>Ads listed for pure marketing purposes without offering any concrete goods, jobs or services are not allowed. You should place your own ads and not place ads on behalf of someone else.</span>
<br /><br /><br />
<h5><b>Ad title</b></h5>
<span>Your ad title should briefly describe what is being advertised, and should not contain prices, salaries, promotional words, contact details or unnecessary search words. Example of promotional words include "promotion", "best deal", "best offer", and "discount".</span>
<br /><br /><br />
<h5><b>Ad description</b></h5>

<span>Your ad description should describe what is being advertised. It should not be copied from other advertisers (this is illegal under copyright laws). It should also not contain unnecessary search words, offensive remarks, or links to other auction/classifieds/marketplace sites. Unnecessary search words are words that do not directly refer to the item itself, but rather refer to closely related items. For example, ads for Nike shoes should not contain the words "Adidas", "Reebok", or "Puma".</span>
<br /><br /><br />
<!-- <h5><b>Ad language</b></h5>
<span>Only ads in English and Bahasa Malaysia are allowed.</span>
<br /><br /><br /> -->
<h5><b>Ad category</b></h5>
<span>You should place your ad in the category that best describes what is being advertised.</span>
<br /><br /><br />
<h5><b>No multiple items in 1 ad</b></h5>
<span>You should only advertise 1 unique item in each ad.</span>
<br /><br /><br />
<h5><b>No duplicates</b></h5>
<span>You should not place the same ad more than once. If you wish to advertise the same item again, please delete your previous ad before you insert a new ad of the same item. Asking friends to post the same ad is also not allowed.</span>
<br /><br /><br />
<h5><b>Links</b></h5>
<span>You can place links that are relevant to your ad in the ad text description. However, you cannot place links to another auction, classifieds, or marketplace site.</span>
<br /><br /><br />
<h5><b>Images</b></h5>
<span>You should add images that are relevant to your ad. Company logos should not be used as images except for the "Services" and "Jobs" categories.</span>
<br />
<br />
<span>An image is not suitable if it:
<ul>
<li>Was taken by another advertiser, and is being reused without their consent (this is illegal under copyright laws)</li>
<li>Contains contact details (phone, email, links, Facebook, or any other personal info)</li>
<li>Contains images of children</li>
<li>Contains promotional words</li>
<li>Contains an advertiser's own watermark of contact details and price</li>
<li>Contains a watermark of a company logo placed in the centre or any of the four corners of the image; or covering more than 30% of the image</li>
<li>Is a collage, i.e. includes multiple images edited into the same image</li>
<li>Is a stock photo</li>
<li>Is downloaded from the Usmers' website</li>
<li>Is not in JPEG, JPG or PNG format</li>
<li>Is reused from any other ad on Usmers'</li>
<li>Is too small or unclear, or is irrelevant</li>
<li>Could be perceived to be offensive or provocative in nature.</li>
</ul>
</span>
<br />

<h5><b>Realistic offers</b></h5>

<span>You should provide the actual selling price or salary in your ad. Usmers' reserves the right to refuse unrealistic selling prices which we do not think are the actual full price of your item (strictly no instalments, down-payments or trade-in prices).</span>
<br /><br /><br />
<h5><b>Bidding and auctions</b></h5>

<span>We do not accept any ads related to auction or bidding. We only accept ads with actual prices.</span>
<br /><br /><br />
<h5><b>Rules concerning pets and animals</b></h5>

<span>Animals traded on Usmers' must follow the Wildlife Protection Act of Malaysia 1972, the Fisheries Act 1985, and CITES.</span>
<br /><br /><br />
<h5><b>Illegal goods</b></h5>
<span>Ads featuring goods that are prohibited from sale according to Malaysian laws are not allowed on Usmers'.</span>
<br /><br /><br />
<h5><b>Prohibited goods and services</b></h5>
<span>Usmers' has restrictions against the advertising of certain goods and services.</span>
<br /><br /><br />
<h5><b>Offensive content</b></h5>
<span>Ads or images that could be perceived as offensive to ethnic groups, individuals, or public figures are not allowed.</span>
<br /><br /><br />
<h5><b>Services</b></h5>
<span>Ads under "Services" must follow applicable laws and regulations of Malaysia for the given profession.</span>
